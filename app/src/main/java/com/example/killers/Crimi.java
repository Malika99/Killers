package com.example.killers;

public class Crimi {

    private String name;
    private String image;

    public Crimi(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    public Crimi()
    {

    }
}
